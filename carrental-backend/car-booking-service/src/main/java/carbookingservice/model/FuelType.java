package carbookingservice.model;

/**
 * Enumeration of supported fuel types for rental cars.
 */
public enum FuelType {

    /**
     * Gasoline fuel.
     */
    GAS,

    /**
     * Electric power.
     */
    ELECTRIC,

    /**
     * Hybrid fuel combining gasoline and electric.
     */
    HYBRID,

    /**
     * Diesel fuel.
     */
    DIESEL,

    /**
     * Petrol fuel.
     */
    PETROL,
}
