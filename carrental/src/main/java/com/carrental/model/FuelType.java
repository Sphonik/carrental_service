package com.carrental.model;

/**
 * Enumeration of supported fuel types for vehicles.
 * <p>
 * Defines the different kinds of propulsion systems a car may use.
 */
public enum FuelType {

    /** Fuel type using gasoline or diesel internal combustion. */
    GAS,

    /** Fuel type using electrical energy stored in batteries. */
    ELECTRIC,

    /** Fuel type combining an internal combustion engine with an electric motor. */
    HYBRID

}
